def calculate_occult_affinity(occult_result: dict) -> dict:
    """占術結果から5タイプそれぞれの親和性スコアを返す。"""
    scores = {"A": 0, "B": 0, "C": 0, "D": 0, "E": 0}
    if not occult_result:
        return scores

    features = {
        "life_path": occult_result.get("numerology", {}).get("life_path", {}).get("number"),
        "shusei": occult_result.get("shusei", {}).get("name", ""),
        "teiou": occult_result.get("teiou", {}).get("name", ""),
        "sun": occult_result.get("western_astrology", {}).get("sun", {}).get("name", ""),
        "doubutsu_12": occult_result.get("doubutsu", {}).get("name_12", ""),
        "kyusei": occult_result.get("kyusei", {}).get("honmei", {}).get("name", ""),
    }

    for type_code, affinity in OCCULT_TYPE_AFFINITY.items():
        for feature_key, value in features.items():
            if value is None or value == "":
                continue
            target_list = affinity.get(feature_key, [])
            if value in target_list:
                scores[type_code] += OCCULT_WEIGHTS[feature_key]

    return scores


def get_strength_level(score: float) -> str:
    """5タイプ判定の点数強度を返す。"""
    if score >= 20:
        return "かなり強く出ているパターン"
    elif score >= 12:
        return "明確に出ているパターン"
    elif score >= 6:
        return "状況によって出るパターン"
    else:
        return "今は抑えているパターン"


def calculate_shinrai_otoko(answers: dict, occult_result: dict | None = None) -> dict:
    """20問×占術重みづけから、5タイプ中のメイン＋隠れタイプを判定する。

    v3.0: 4タイプ→5タイプに転換。各タイプは「失敗パターン型」。
    """
    question_scores = {"A": 0, "B": 0, "C": 0, "D": 0, "E": 0}
    for q in QUESTIONS:
        ans = answers.get(q["id"])
        if ans in question_scores:
            question_scores[ans] += 1

    occult_scores = (
        calculate_occult_affinity(occult_result)
        if occult_result
        else {"A": 0, "B": 0, "C": 0, "D": 0, "E": 0}
    )

    combined_scores = {
        t: occult_scores[t] + question_scores[t] * QUESTION_WEIGHT
        for t in ("A", "B", "C", "D", "E")
    }

    # 同点時の優先順位は A>B>C>D>E（仕様書の並び順を踏襲）
    sorted_types = sorted(combined_scores.items(), key=lambda x: (-x[1], x[0]))
    main_code, main_score = sorted_types[0]
    second_code, second_score = sorted_types[1] if len(sorted_types) > 1 else (None, 0)

    main_profile = TYPE_PROFILES.get(main_code, {})
    second_profile = TYPE_PROFILES.get(second_code, {}) if second_code else {}

    return {
        "type": main_code,
        "name": main_profile.get("name", ""),
        "tagline": main_profile.get("tagline", ""),
        "tag_short": main_profile.get("tag_short", main_profile.get("tagline", "")),
        "tag_role": main_profile.get("tag_role", main_profile.get("tagline", "")),
        "tag_essence": main_profile.get("tag_essence", main_profile.get("tagline", "")),
        "strength_body": main_profile.get("strength_body", ""),
        "shadow_body": main_profile.get("shadow_body", ""),
        "wife_view": main_profile.get("wife_view", ""),
        "key_shift": main_profile.get("key_shift", ""),
        "score": main_score,
        "strength_level": get_strength_level(main_score),
        "second_type": second_code,
        "second_name": second_profile.get("name", ""),
        "second_tagline": second_profile.get("tagline", ""),
        "second_tag_short": second_profile.get("tag_short", second_profile.get("tagline", "")),
        "second_strength_body": second_profile.get("strength_body", ""),
        "second_shadow_body": second_profile.get("shadow_body", ""),
        "second_score": second_score,
        "all_scores": combined_scores,
        "question_scores": question_scores,
        "occult_scores": occult_scores,
    }


